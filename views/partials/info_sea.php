<h2>🐙 Ab ins kühle Nass!</h2>
<p>
    Entdecke unsere schillernde Übersicht aller 40 Meerestiere in <i>Animal Crossing: New Horizons</i>. 
    Alles, was du brauchst, um abzutauchen, ist ein schicker <strong>Taucheranzug</strong>!
</p>
<p>
    <strong>Anschleichen für Profis:</strong> Manche Meeresbewohner sind extrem flink. Du kannst dich "anschleichen", indem du nur durch sanftes Bewegen des linken Sticks schwimmst, ganz ohne den A-Knopf zu drücken. Befindest du dich dann direkt über dem Meerestier (an der Oberfläche wunderschön an Bläschen zu erkennen), kannst du es mit einem beherzten Tauchvorgang direkt fangen.
</p>
<p>
    Natürlich kannst du auch Meerestiere stilvoll in ihren Behältnissen in deinem Haus oder auf der Insel platzieren. 
    <strong>Achtung:</strong> Leider kannst du von Meerestieren keine Modelle in Auftrag geben lassen.
</p>