<h2>🐟 Petri Heil!</h2>
<p>
    Tauche ein in unsere komplette Übersicht aller 80 schuppigen Inselbewohner. 
    Zücke deine <strong>Angel</strong>, aber bemühe dich, nicht direkt am Ufer zu rennen, um deine wertvolle Beute nicht zu verscheuchen!
</p>
<p>
    <strong>Der Köder-Trick:</strong> Buddelst du am Strand Teppichmuscheln aus, kannst du pro Muschel einen Köder basteln (die Anleitung lernst du beim ersten Ausgraben völlig automatisch). Köder lassen sich an sämtlichen Gewässern einsetzen und lassen dort sofort einen zufälligen, zur Uhrzeit passenden Fisch erscheinen. Das Beste daran: Ein Köder garantiert dir einen Fisch und schließt Müll komplett aus!
</p>
<p>
    <strong>Müll-Recycling:</strong> Ohne Köder angelst du manchmal Steine, Stiefel, Dosen oder Reifen. Kein Grund zum Ärgern! Diese kannst du für fantastische Bastelanleitungen verwenden, die du beim Angeln von unterschiedlichen Müllmengen freischaltest.
</p>
<p>
    Gefangene Fische machen sich auch hervorragend als Deko in Aquarien. Und genau wie bei den Insekten gilt: Ein kurzer Abstecher in ein Gebäude lädt alle Fische neu.
</p>