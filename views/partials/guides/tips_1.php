    <ul>
        <li><strong>Du selbst:</strong> ~5% Chance</li>
        <li><strong>1 Besucher:</strong> ~25% Chance</li>
        <li><strong>5 Besucher:</strong> ~80% Chance (Blumen glitzern golden!)</li>
    </ul>
