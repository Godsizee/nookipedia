<div class="tip-card">
    <h3>👯 Das Klonen</h3>
    <p>Wenn du eine seltene Farbe (wie Lila oder Blau) hast und mehr willst, stelle die Blume <strong>alleine</strong> auf ein Feld (kein Kontakt zu anderen Blumen). Gießt du sie, klont sie sich mit einer gewissen Wahrscheinlichkeit exakt selbst.</p>
</div>
<div class="tip-card">
    <h3>📏 Layout & Sauberkeit</h3>
