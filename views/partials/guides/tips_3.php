    <h3>💧 Das Gieß-System (Besucher-Bonus)</h3>
    <p>Alleine gießen bringt nur eine geringe Chance auf Vermehrung. Wenn <strong>5 Besucher</strong> gießen, erreichst du eine riesige Chance (~80%) und das "Goldene Glitzern". Suche dir Freunde oder Online-Communities, die helfen!</p>
</div>
<div class="tip-card">
    <h3>👯 Klonen von Grünen Chrysanthemen</h3>
    <p>Sobald du eine einzige grüne Chrysantheme hast, solltest du sie isolieren. Stelle sie so auf, dass sie keine andere Chrysantheme berührt, und gieße sie täglich. Da kein Partner involviert ist, ist das geklonte Kind garantiert ebenfalls Grün.</p>
</div>
<div class="tip-card">
    <h3>📏 Organisierte Beete</h3>
    <p>Verwende ein Schachbrettmuster für die Kreuzung (z.B. Lila - Platz - Lila). Markiere Hybrid-Gelbe Beete mit einem speziellen Design auf dem Boden, damit du sie nicht aus Versehen mit normalen gelben Blumen verwechselst.</p>
</div>
<div class="tip-card">
    <h3>🗑️ Entsorgung</h3>
    <p>Alles, was in deinem "Lila (Hybrid)"-Beet wächst und weiß ist, sollte sofort entfernt werden, da es absolut keine grünen Gene weitergeben kann.</p>
</div>
