<div class="tip-card">
    <h3><span class="tip-icon">👯</span> Grünes Klonen</h3>
    <div class="tip-content">
        <p>Sobald du eine einzige grüne Chrysantheme hast, solltest du sie <strong>isolieren</strong> (keine andere Chrysantheme berühren).</p>
        <div class="tip-highlight">Da kein Partner involviert ist, ist das geklonte Kind garantiert ebenfalls Grün.</div>
    </div>
