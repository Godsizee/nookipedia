<div class="tip-card">
    <h3><span class="tip-icon">💧</span> Gieß-System</h3>
    <div class="tip-content">
        <p>Um die Chance auf Nachwuchs zu maximieren, ist Gießen essenziell. Nur du gießt = geringe Chance.</p>
        <div class="tip-highlight">Lass bis zu <strong>5 Freunde</strong> gießen für das "große goldene Glitzern" und eine extrem hohe Wahrscheinlichkeit (~80%)!</div>
    </div>
</div>
<div class="tip-card">
    <h3><span class="tip-icon">👯</span> Klonen</h3>
    <div class="tip-content">
        <p>Wenn du deine erste schwarze Cosmea erhalten hast, solltest du sie <strong>isoliert aufstellen</strong>.</p>
        <div class="tip-highlight">Gießt du sie, kopiert sie sich selbst. Der sicherste Weg, um ein schwarzes Beet zu erhalten, ohne auf die 6,25% Chance angewiesen zu sein.</div>
    </div>
</div>
<div class="tip-card">
    <h3><span class="tip-icon">📏</span> Das Paar-System</h3>
    <div class="tip-content">
        <p>Statt eines großen Schachbrettmusters empfehlen Experten für Cosmeen das <strong>Paar-System</strong>.</p>
        <div class="tip-highlight">Platziere zwei orange Blumen direkt nebeneinander und lasse drumherum Platz, um ungewollte Kreuzungen zu verhindern.</div>
    </div>
</div>
<div class="tip-card">
    <h3><span class="tip-icon">🧹</span> Beet-Sauberkeit</h3>
    <div class="tip-content">
        <p>Entferne gelbe oder weiße "Fehlzuchten" aus deinem orangen Zuchtbeet <strong>sofort</strong>.</p>
        <div class="tip-highlight">Sonst blockieren sie den wertvollen Platz für potenzielle schwarze Knospen.</div>
    </div>
</div>
