<div class="tip-card">
    <h3>💧 Das Gieß-System</h3>
    <p>Um die Chance auf Nachwuchs zu maximieren, ist Gießen essenziell. Nur du gießt = geringe Chance. Lass bis zu <strong>5 Freunde</strong> gießen für das "große goldene Glitzern" und eine extrem hohe Wahrscheinlichkeit (~80%)!</p>
</div>
<div class="tip-card">
    <h3>👯 Klonen</h3>
    <p>Wenn du deine erste schwarze Cosmea erhalten hast, solltest du sie isoliert aufstellen (kein Kontakt zu anderen Cosmeen). Gießt du sie, wird sie sich selbst kopieren. Das ist der sicherste Weg, um ein schwarzes Beet zu erhalten, ohne auf die 6,25% Chance angewiesen zu sein.</p>
</div>
<div class="tip-card">
    <h3>📏 Layout-Empfehlung: Das Paar-System</h3>
    <p>Statt eines großen Schachbrettmusters empfehlen Experten für Cosmeen das Paar-System. Platziere zwei orange Blumen direkt nebeneinander und lasse drumherum Platz, um ungewollte Kreuzungen zu verhindern.</p>
</div>
<div class="tip-card">
    <h3>🧹 Sauberkeit im Beet</h3>
    <p>Entferne gelbe oder weiße "Fehlzuchten" aus deinem orangen Zuchtbeet sofort, damit diese nicht den Platz für potenzielle schwarze Knospen blockieren.</p>
</div>
