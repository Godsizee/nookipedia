<div class="museum-intro-box">
    <p>
        Das Museum wird von <strong>Eugen</strong> geleitet. Es ist täglich 24 Stunden geöffnet und der Eintritt ist natürlich kostenlos! Eugen nimmt deine Spenden liebend gern entgegen (es sei denn, du bringst ihm Insekten...) und stellt sie liebevoll im Gebäude aus.
    </p>
    <p>
        Das Museum ist in faszinierende Bereiche unterteilt: das <strong>Aquarium</strong> (Fische und Meerestiere), die <strong>Insektenausstellung</strong>, die <strong>Fossiliengalerie</strong> und die <strong>Kunstgalerie</strong>. Zudem befindet sich im ersten Stock das legendäre <strong>Café Taubenschlag</strong>.
    </p>
</div>

<div class="museum-section-header" style="margin-top: 3.5rem;">
    <h2>Freischaltungen & Geheimnisse</h2>
    <p>So erweiterst du dein Museum Schritt für Schritt zur absoluten Perfektion.</p>
</div>

<div class="museum-guide-grid">
    <div class="museum-guide-card">
        <h3>⛺ Vom Zelt zum Museum</h3>
        <ul>
            <li><strong>Eugens Zelt:</strong> Gib Tom Nook zu Beginn des Spiels 5 verschiedene Fische oder Insekten. Daraufhin lädt er Eugen auf deine Insel ein. Am nächsten Tag schlägt dieser sein Zelt auf.</li>
            <li><strong>Das Museumsgebäude:</strong> Gib Eugen im Zelt 15 weitere, verschiedene Exponate (Fische, Insekten oder Fossilien). Nach diesen 15 Spenden wird am darauffolgenden Tag das echte Museum gebaut.</li>
        </ul>
    </div>
    
    <div class="museum-guide-card">
        <h3>🦊 Die Kunstgalerie</h3>
        <ul>
            <li>Sobald du insgesamt <strong>60 verschiedene Exponate</strong> gespendet hast, weist Eugen dich darauf hin, dass er nun auch Kunstwerke annehmen möchte.</li>
            <li>Am nächsten Tag warnt Melinda in den Insel-News vor einem „zwielichtigen Händler“. Suche <strong>Reiner</strong> auf deiner Insel und kaufe dein erstes (echtes) Kunstwerk von ihm.</li>
            <li>Spende dieses Kunstwerk bei Eugen. Das Museum wird am nächsten Tag um das Obergeschoss erweitert!</li>
        </ul>
    </div>

    <div class="museum-guide-card">
        <h3>☕ Café Taubenschlag</h3>
        <ul>
            <li>Deine Insel muss eine Bewertung von <strong>mindestens 3 Sternen</strong> haben und K.K. Slider muss bereits aufgetreten sein.</li>
            <li>Du musst in allen 5 Kategorien (Fische, Insekten, Meerestiere, Fossilien, Kunst) <strong>jeweils mindestens ein Exponat</strong> gespendet haben.</li>
            <li>Sprich mit Eugen. Er bittet dich, seinen Freund Kofi zu suchen, und gibt dir ein Foto.</li>
            <li>Fahre mit <strong>Käpten</strong> auf eine Bootstour. Dort triffst du Kofi. Richte ihm Eugens Grüße aus und kehre zurück. Das Museum wird am Folgetag umgebaut.</li>
        </ul>
    </div>

    <div class="museum-guide-card">
        <h3>🖼️ Die Lithografien (Poster)</h3>
        <p class="guide-subtitle">Lithografien sind spezielle Wandmöbel, die du als Belohnung für deine harte Arbeit erhältst:</p>
        <ul>
            <li>Sobald du einen Bereich <strong>zu 100% komplettiert</strong> hast (alle Fische, alle Insekten, etc.), schickt dir Eugen am nächsten Tag die entsprechende Lithografie per Post zu.</li>
            <li><em>Experten-Tipp:</em> Hast du eine Kategorie bereits vor dem großen Update 2.0 abgeschlossen? Dann musst du Eugen einfach einmal im Museum ansprechen, damit er sie dir nachträglich zuschickt!</li>
        </ul>
    </div>
